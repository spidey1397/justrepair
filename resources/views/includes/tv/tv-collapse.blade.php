        <a class="collapsed card-link" data-toggle="collapse" href="#collapseTwo">

          <h2 class="lead text-light">TV shows a blue, green, or black screen</h2>

        </a>

      </div>

      <div id="collapseTwo" class="collapse" data-parent="#accordion">

        <div class="card-body">

          <p>Your television is not getting a signal. Make sure that your cable or satellite box (or other device) is powered on and that the TV is set to the correct input – press Input, Source or TV/Video on your remote control. Check behind the TV for loose connections. If none of this works and you are trying to watch live TV, then it is probably time to contact your cable or satellite provider. You may need to “reset” your receiver. If it is a DVD player, the problem may be the player. Also, the video cable may be damaged.</p>

        </div>

      </div>

    </div>



    <div class="card">

      <div class="card-header bg-greeen">

        <a class="collapsed card-link" data-toggle="collapse" href="#collapseThree">

          <h2 class="lead text-light">TV image is “breaking up” or showing pixelation</h2>

        </a>

      </div>

      <div id="collapseThree" class="collapse" data-parent="#accordion">

        <div class="card-body">

          <p>This is caused by a weak signal. In some cases, it can be the satellite signal between a live event and the television station (especially if you are watching live sports on the other side of the world). If it happens only when you attempt to watch HD, then it might be a compression artifact. In other words, this is a reception problem, not a problem with your TV. If it happens a lot, you can try reducing your sharpness setting.</p>

        </div>

      </div>

    </div>

    <div class="card">

      <div class="card-header bg-greeen">

        <a class="collapsed card-link" data-toggle="collapse" href="#collapseFour">

          <h2 class="lead text-light">TV will turn on from the remote, but not the cable or satellite box</h2>

        </a>

      </div>

      <div id="collapseFour" class="collapse" data-parent="#accordion">

        <div class="card-body">

          <p>The cable box is not talking to your TV properly. Make sure that this is an option (it isn’t with all receivers) and if it is, try turning the box off, unplugging it for 15 seconds (at least) and turning it back on again.</p>

      </div>

